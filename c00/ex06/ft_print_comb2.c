#include<unistd.h>
void	print(char x)
{
	write(1, &x, 1);
}

void	ft_print_comb2(void)
{	
	int	y;	
	int	z;

	y = 0 ;
	while (y <= 98)
	{
		z = y + 1 ;
		while (z <= 99)
		{
			print(y / 10 + '0');
			print(y % 10 + '0');
			print(' ');
			print(z / 10 + '0');
			print(z % 10 + '0');
			if ((y != 98) || (z != 99))
			{
				print(',');
				print(' ');
			}
			z++;
		}
		y++;
	}
}
